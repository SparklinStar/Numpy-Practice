import numpy as np

# Create a sample NumPy array
arr = np.array([10, 20, 30, 40, 50])

# Define the desired index order
index_order = np.array([2, 0, 4, 1, 3])

# Rearrange the array based on the specified index order
rearranged_arr = arr[index_order]

print("Original array:")
print(arr)
print("Rearranged array:")
print(rearranged_arr)
